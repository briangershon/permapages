  <h2 class="mt-4">The frontier of Arweave</h2>
  <p>
    There is a confluence of invention happening in the Arweave community at an
    accelerated pace, connecting various individuals and entities with the
    result of fresh technology that may define the next generation of the "web"
    as we know it. More specifically, what Sam Williams refers to as "The Web of
    Value". We spoke at length on the topic, exploring the latest developments
    to better understand what this all means and where it may go.
  </p>

  <p class="mt-4">
    The "stamp protocol" can be seen as a system of reward points. The recent
    development of burned AR, or bAR may be an exchange mechanism that can be
    driven by smartweave contracts on Arweave. In addition to the above,
    embedded marketplaces within an individual NFT is also now becoming a
    reality with Verto Flex. There are specific limitations that developers can
    add via add pair that is essentially a gate that says "I want this
    contracted to be traded with x OR I don't want this to be traded with
    anything".
  </p>

  <p class="mt-4">
    When thinking about creating a token, it can make a lot of sense to consider
    them as "credits" or "rewards" much like airline miles. These are widely
    used today and have become an integral part of incentive design campaigns
    and as tools for customers. One can also point to the "timeshare model",
    where there is a point system and one can exchange points as needed. The
    full intention here is to keep it lawful and akin to these programs that are
    used by millions of individuals in the US and beyond.
  </p>

  <p class="mt-4">
    What's extraordinary is that creators will now have access to create entire
    systems with just one piece of content. The creator can earn rewards by
    individuals viewing or "stamping" their content, fans or collectors can
    influence discovery of these items, and entire communities can operate with
    "rewards" as a driving force via incentives.
  </p>

  <p class="mt-4">
    Community recommendation: Stay in the camp of miles and credits as much as
    possible when it comes to the aforementioned. There are established laws
    that enable this activity on a massive scale that large corporations use on
    a daily basis. There are some interesting ways to remove speculation and
    have utility while building a public good, like building social mechanisms.
  </p>

